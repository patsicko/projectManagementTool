import mongoose from "mongoose"
import dotenv from "dotenv";

const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
});


const User = mongoose.model('User', userSchema);

export default User;
